package fundamentos;

//Classe: Variaveis
public class Variaveis {

	public static void main(String[] args) {

		final double PI = 3.1415; // Constante ("final" - nao deixa o valor ser alterado)
		float area = 2; // Variavel

		System.out.println("A area da circunferencia de um circulo e: " + PI * area * area);
	}

}
